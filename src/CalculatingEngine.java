public class CalculatingEngine {
    public static int doSum(int A,int B) {
        return(A+B);
    }
    public static int doDiff(int A,int B) {
        return(A-B);
    }
    public static int doMutliply(int A,int B){
        return(A*B);
    }
    public static double doDivide(int A,int B){
        return(A/B);
    }
}
